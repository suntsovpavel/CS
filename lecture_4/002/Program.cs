//Эта функция реализует один из алгоритмов закраски замкнутых областей
//(вся область pic внутри замкнутого контура заполняется плюсиками)
void FillImage(int row, int col)
{
    if (pic[row, col] == 0)
    {
        pic[row, col] = 1;
        FillImage(row - 1, col);
        FillImage(row, col - 1);
        FillImage(row + 1, col);
        FillImage(row, col + 1);
    }
}
